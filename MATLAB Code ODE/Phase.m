
t = 100;
y1 = zeros(1,t);
y2 = zeros(1,t);
y3 = zeros(1,t);

r1 = 0.1;
r2 = 0.5;


for a = 1:0.1:2
    for b = 1:0.1:2
        for c = 0.1:0.1:1
            y1(1) = a;
            y2(1) = b;
            y3(1) = c;
            for n = 2:t
                y1(n) = (0.001/2)*((1-r1)*y1(n-1)*y3(n-1)+ (1-r2)*y2(n-1)*y3(n-1)-2*r1*y1(n-1)*y1(n-1)+ (1-r1)*y1(n-1) - 2*r2*y2(n-1)*y1(n-1) );
                y2(n) = (0.001/2)*((1-r2)*y2(n-1)*(1-y3(n-1)) + (1-r1)*y1(n-1)*(1-y3(n-1)) - 2*r2*y2(n-1)*y2(n-1) + (1-r2)*y2(n-1) - 2*y1(n-1)*y2(n-1)*r1);
                y3(n) = (0.001/2)*(-r1*y1(n-1)*y3(n-1)-r2*y2(n-1)*y3(n-1) + r1*y1(n-1));
            end
            plot3(y1,y2,y3)
            hold on
            
        end 
    end 
end

